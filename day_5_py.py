def create_stacks_list(input_list):
    stacks_list = []
    for ii in range(0,len(input_list[0]),4):
        stacks_list.append([])
    return stacks_list

def moves_str(input_list):
    for ii in range(len(input_list)):
        if input_list[ii] == '':
            return input_list[ii+1:]

def create_moves(input_list):
    list_of_moves = moves_str(input_list)
    moves_cmd = []
    for each in list_of_moves:
        # each.split()
        each = each.split()
        move = int(each[1])
        crate1 = int(each[3])
        crate2 = int(each[5])
        moves_cmd.append([move,crate1,crate2])
    return moves_cmd 

def pop_stacks(input_list):
    stacks_list = create_stacks_list(input_list)
    for row in input_list:
        # print(row)
        if row == '':
            break
        count = 0
        for ii in range(0,len(row),4):
            crate = row[ii:ii+3].strip('[]')
            if row[ii] == ' ':
                count+=1
                continue
            else:
                stacks_list[count].append(crate)
                count+=1
    return stacks_list

